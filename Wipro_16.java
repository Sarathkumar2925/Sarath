package wipro;
import java.util.*;
public class Wipro_16 {
	public static void main(String[] args) {
		Scanner s=new Scanner(System.in);
		int n=s.nextInt();
		int i=1;
		while(i<=n)	
		{
			System.out.print("");
			int j=1;
			while(j<=i)
			{
				System.out.print(" * ");
				j++;
			}
			System.out.println();
			i++;
			s.close();
	}
}
}
